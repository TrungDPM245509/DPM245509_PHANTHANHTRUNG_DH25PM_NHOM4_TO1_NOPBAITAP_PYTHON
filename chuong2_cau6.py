'''
Câu 6:Trình bày ý nghĩa toán tử /, //, %, **, and, or, is.

Trả lời:

1. Toán tử / (Chia thông thường)
Ý nghĩa: Dùng để chia hai số và trả về kết quả là một số thực (float).

2. Toán tử // (Chia lấy phần nguyên)
Ý nghĩa: Dùng để chia hai số và trả về phần nguyên của kết quả, tức là phần nguyên của phép chia.

3. Toán tử % (Chia lấy dư)
Ý nghĩa: Dùng để tính phần dư của phép chia giữa hai số.

4. Toán tử ** (Lũy thừa)
Ý nghĩa: Dùng để tính lũy thừa của một số, tức là một số được nâng lên một lũy thừa nhất định.

5. Toán tử and (Và logic)
Ý nghĩa: Toán tử logic and trả về True nếu cả hai biểu thức đều đúng (True), ngược lại trả về False.

6. Toán tử or (Hoặc logic)
Ý nghĩa: Toán tử logic or trả về True nếu ít nhất một trong hai biểu thức là True.

7. Toán tử is (So sánh đối tượng)
Ý nghĩa: Toán tử is dùng để kiểm tra xem hai đối tượng có phải là cùng một đối tượng trong bộ nhớ hay không (so sánh theo địa chỉ bộ nhớ, không phải giá trị).

'''